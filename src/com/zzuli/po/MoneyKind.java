package com.zzuli.po;

public class MoneyKind {
	private String kind_id;
	private String kind_code;
	private String kind_name;
	private String kind_item;
	public String getKind_id() {
		return kind_id;
	}
	public void setKind_id(String kind_id) {
		this.kind_id = kind_id;
	}
	public String getKind_code() {
		return kind_code;
	}
	public void setKind_code(String kind_code) {
		this.kind_code = kind_code;
	}
	public String getKind_name() {
		return kind_name;
	}
	public void setKind_name(String kind_name) {
		this.kind_name = kind_name;
	}
	public String getKind_item() {
		return kind_item;
	}
	public void setKind_item(String kind_item) {
		this.kind_item = kind_item;
	}
	
}
